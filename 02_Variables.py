"""
EJEMPLO 2: VARIABLES Y TIPOS DE DATOS
-------------------------------------
Las variables son contenedores que almacenan datos.
Python tiene varios tipos de datos básicos...
"""

# Números enteros (int)
edad = 25
cantidad = 100

# Números decimales (float)
precio = 19.99
temperatura = 36.5

# Texto (string)
nombre = "Juan"
apellido = "Pérez"
mensaje = "Bienvenido a Python"

# Valores booleanos (bool)
es_estudiante = True
es_profesor = False

# None (sin valor)
dato = None

# Imprimir las variables
print(f"Nombre: {nombre}")
print(f"Edad: {edad}")
print(f"Precio: ${precio}")
print(f"¿Es estudiante? {es_estudiante}")

# Verificar el tipo de una variable
print(f"Tipo de 'edad': {type(edad)}")
print(f"Tipo de 'precio': {type(precio)}")
print(f"Tipo de 'nombre': {type(nombre)}")
print(f"Tipo de 'es_estudiante': {type(es_estudiante)}")

